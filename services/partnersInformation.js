const mm = require('../utilities/globalModule');
const { validationResult, body } = require('express-validator');
const logger = require("../utilities/logger");

const applicationkey = process.env.APPLICATION_KEY;

var partnersInformation = "partners_information";
var viewPartnersInformation = "view_" + partnersInformation;


function reqData(req) {

    var data = {
        FIRM_INFORMATION_ID: req.body.FIRM_INFORMATION_ID,
        NAME: req.body.NAME,
        PAN_NUMBER: req.body.PAN_NUMBER,
        AADHAR: req.body.AADHAR,
        DIRECTORS_IDENTITY_NUMBER: req.body.DIRECTORS_IDENTITY_NUMBER,
        MOBILE_NUMBER: req.body.MOBILE_NUMBER,
        GENDER: req.body.GENDER,
        DOB: req.body.DOB,
        CASTE: req.body.CASTE,
        RELIGION: req.body.RELIGION,
        EDUCATION: req.body.EDUCATION,
        MEMBERSHIP_NUMBER: req.body.MEMBERSHIP_NUMBER,
        RELATIONSHIP_WITH_OTHER: req.body.RELATIONSHIP_WITH_OTHER,
        IS_AUTHORISED_TO_TRANSACT: req.body.IS_AUTHORISED_TO_TRANSACT ? '1' : '0',

        CLIENT_ID: req.body.CLIENT_ID

    }
    return data;
}



